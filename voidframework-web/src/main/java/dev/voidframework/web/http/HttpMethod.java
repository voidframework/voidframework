package dev.voidframework.web.http;

/**
 * Defines handled HTTP method.
 *
 * @since 1.0.0
 */
public enum HttpMethod {

    /**
     * @since 1.0.0
     */
    DELETE,

    /**
     * @since 1.0.0
     */
    GET,

    /**
     * @since 1.0.0
     */
    HEAD,

    /**
     * @since 1.0.0
     */
    OPTIONS,

    /**
     * @since 1.0.0
     */
    POST,

    /**
     * @since 1.0.0
     */
    PUT,

    /**
     * @since 1.3.0
     */
    WEBSOCKET
}
